#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_numbers(void)
{
	char	my_character;

	my_character = '0';
	while (my_character <= '9')
		ft_putchar(my_character++);
}
