#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_reverse_alphabet(void)
{
	char	my_character;

	my_character = 'z';
	while (my_character >= 'a')
		ft_putchar(my_character--);
}
